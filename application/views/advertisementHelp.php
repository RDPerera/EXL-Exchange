        Creating <span class="dark-title">an Advertisment&nbsp&nbsp</span>
      </p>
      <br><br>
    
        When you become a seller on EXL Exchange, we recommend creating one Advertisement in the beginning. Focus your time and effort into one service. Follow the guidelines below to make your Advertisement stand out. Once your Advertisement generates customer feedback and sales, you should consider creating more Advertisements. 

        <br><br><p class="sub-title">Title</p>
        
        Your Advertisement title should be short, clear and to the point. EXL Exchange helps you start your title with ”I will.” Simply complete the sentence.
        <br><br><p class="sub-title">Category/subcategory</p>
        
        Select the appropriate category and subcategory for the service you are offering. If you’re not sure, you can search EXL Exchange’s marketplace for similar services. EXL Exchange will recommend a category/subcategory based on your Advertisement title.
        
        Note: EXL Exchange reserves the right to move your Advertisement to a more relevant category/subcategory.
        <br><br><p class="sub-title">Scope & pricing (Advertisement Packages)</p>
        Advertisement packages allow you to offer services in different formats and prices. You can create up to three packages (Basic, Standard, and Premium) for a different price.
        Each Advertisement package should include different service elements. This includes description, delivery time, revisions, etc. Adding extras for an additional price to your Advertisement packages can you help upsell your services. 
        
        <br><br><p class="sub-title">Description</p>
        This is your opportunity to introduce yourself to potential customers. Be personable and describe your service to guide customers to make a purchase. For more information, see How to Make Your EXL Exchange Profile Stand Out.
        
        When writing your Advertisement description, you should:
        Be clear about your Advertisement’s purpose and the service you are providing. Please be sure that your Advertisement is in line with our Terms of Service. 
        Always proofread your Advertisement description before publishing. 
        Advertisement descriptions in English are mandatory. You can use additional languages as long as all the information is also available in English.
        Avoid using external links that are not a part of our allowed links.
        Don't include your personal contact information. If contact or login credentials to external sites are absolutely necessary for your Advertisement service, please make sure to mention this in your Advertisement requirements.
        Tell your customer what you need to get started; you should list this information clearly in the Advertisement requirements area (if the service you are providing cannot be completed without specific information from the buyer).
        
        Media
        Only use images, videos, audio or other content fully owned by you. The media you use may also include samples of your work. 
        <br><br><p class="sub-title">Advertisment Images</p>

<p class="ans">
      Images are one of the most crucial factors that will determine whether or not buyers will want to click on your AD and order your service. 
    The following article should give you the very basic guidelines that will help you shine and fit the quality standards of EXL.
     Please be aware that not following these guidelines can not only push buyers away from your business but also prevent you from being manually promoted by EXL across the site.
     </p>
     <b>When using images, please consider the following:</b><br><br>
     <div class="subans">
     
        <ul>
        <li>Quality</li>
        Dont use pixelated images.pixelated image are often low quality and potential buyers may believe this means you offer a low quality AD . use high-res images 
        for the best results.
        <br>
        <br>
        <li>Copyrights</li>
        dont use stock images or other people's work if you dont have usage rights.
        not only could you be prosecuted ,but it is just uncool.
        <br>
        <br>
        <li>Clickbait</li>
        dont use clickbait .using images for the sake of clicks can cause frustration and may even put your AD 
        at risk with EXL intergrity team and lower your rankings.
        <br>
        <br>
        <li>Text</li>
        Dont use too much text on am image.keep the space clean soyour primary will
        pop.if you must use a lot of text,just make sure it's legible.
        <br>
        <br>
        <li>Density</li>
        Dont overcrowed with images either.
        <br>
        <br>
        <li>Proportions</li>
        if you own more than a single AD, be sure to choose a different image to each one ,this will ensure buyers can find what they need.
        <br>
        <br>
        <li>Uniqueness</li>
        use the right image proportions , dont stretch,rotate or use images that are too small. 
        <br>
        <br>
        <li>Relevancy</li>
        Make sure your AD image is related to the service you are offering .irrelevant image 
        not only compromises your ad's credibility,but also lower chances of getting promoted in editorial features.
